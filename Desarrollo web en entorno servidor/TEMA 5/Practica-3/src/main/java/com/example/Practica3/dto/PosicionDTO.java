package com.example.Practica3.dto;

public class PosicionDTO {
    private int x;
    private int y;

    public PosicionDTO() {
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
